# Datatype examples

# String
my_string = "This is a string."
print(f"String: {my_string}")

# Integer
my_integer = 10
print(f"Integer: {my_integer}")

# Float
my_float = 3.14
print(f"Float: {my_float}")

# Complex
my_complex = 1j + 2
print(f"Complex: {my_complex}")

# List
my_list = ["apple", "banana", "cherry"]
print(f"List: {my_list}")

# Tuple
my_tuple = ("apple", "banana", "cherry")
print(f"Tuple: {my_tuple}")

# Range
my_range = range(5)
print(f"Range: {my_range}")

# Dictionary
my_dictionary = {"apple": "red", "banana": "yellow", "cherry": "red"}
print(f"Dictionary: {my_dictionary}")

# Set
my_set = {"apple", "banana", "cherry"}
print(f"Set: {my_set}")

# Frozenset
my_frozenset = frozenset({"apple", "banana", "cherry"})
print(f"Frozenset: {my_frozenset}")

# Boolean
my_boolean = True
print(f"Boolean: {my_boolean}")

# Bytes
my_bytes = b"Hello, world!"
print(f"Bytes: {my_bytes}")

# Bytearray
my_bytearray = bytearray(b"Hello, world!")
print(f"Bytearray: {my_bytearray}")

# Memoryview
my_memoryview = memoryview(b"Hello, world!")
print(f"Memoryview: {my_memoryview}")

# None
my_none = None
print(f"None: {my_none}")
